DROP TABLE IF EXISTS Employee;

create table EMPLOYEE (
	id INT,
	name VARCHAR(150),
	email VARCHAR(150),
	blood_type VARCHAR(150),
	gender VARCHAR(150),
	ip_address VARCHAR(120),
	phone VARCHAR(150),
	company_name VARCHAR(150),
	job_title VARCHAR(150),
	university VARCHAR(150),
	ssn VARCHAR(150)
);
